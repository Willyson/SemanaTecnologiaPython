CREATE TABLE IF NOT EXISTS `DB_STPYTHON`.`ENDERECO` (
  `ID_ENDERECO`     INT NOT NULL AUTO_INCREMENT,
  `CEP_ENDERECO`    INT  NULL,
  `BAIRRO_ENDERECO` VARCHAR(250)  NULL,
  `CIDADE_ENDERECO` VARCHAR(250)  NULL,
  `UF_ENDERECO`     CHAR(2) NULL,
  `ID_USUARIO`      INT NULL,
